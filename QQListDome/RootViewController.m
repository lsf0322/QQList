#pragma mark - View lifecycle

- (void)viewDidLoad
{
    [super viewDidLoad];
    array=[[NSMutableArray alloc]init];
    for(NSInteger section='A';section<='Z';section++)
    {
        NSMutableArray *arr=[NSMutableArray arrayWithCapacity:10];
        for(int i=0;i<10;i++)
        {
            NSMutableDictionary *dir=[NSMutableDictionary dictionaryWithCapacity:0];
            
            NSString *title=[NSString stringWithFormat:@"%c%d",section,i];
            [dir setObject:title forKey:COUNT_STR];
            
            NSString *title1=[NSString stringWithFormat:@"%c",section];
            [dir setObject:title1 forKey:GROUP_NAME];
            
            [arr addObject:dir];
        }
        [array addObject:arr];
    }
}

